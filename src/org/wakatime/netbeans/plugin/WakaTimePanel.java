    void load() {
        // TODO read settings and initialize GUI
        // Example:
        // someCheckBox.setSelected(Preferences.userNodeForPackage(WakaTimePanel.class).getBoolean("someFlag", false));
        // or for org.openide.util with API spec. version >= 7.4:
        // someCheckBox.setSelected(NbPreferences.forModule(WakaTimePanel.class).getBoolean("someFlag", false));
        // or:
        // someTextField.setText(SomeSystemOption.getDefault().getSomeStringProperty());
        
        apiKeyField.setText(WakaTime.getApiKey());
        
        if (WakaTime.isDebugEnabled()) {
            debugToggle.setSelected(true);
            debugToggle.setText("ON");
        } else {
            debugToggle.setSelected(false);
            debugToggle.setText("OFF");
        }
        
    }

    void store() {
        // TODO store modified settings
        // Example:
        // Preferences.userNodeForPackage(WakaTimePanel.class).putBoolean("someFlag", someCheckBox.isSelected());
        // or for org.openide.util with API spec. version >= 7.4:
        // NbPreferences.forModule(WakaTimePanel.class).putBoolean("someFlag", someCheckBox.isSelected());
        // or:
        // SomeSystemOption.getDefault().setSomeStringProperty(someTextField.getText());

        String apiKey = apiKeyField.getText();
        ConfigFile.set("settings", "api_key", apiKey);
        NbPreferences.forModule(WakaTime.class).put("API Key", apiKey);
        
        String debug = "false";
        if (debugToggle.isSelected())
            debug = "true";
        ConfigFile.set("settings", "debug", debug);
        NbPreferences.forModule(WakaTime.class).put("Debug", debug);
    }

